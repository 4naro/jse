package com.example.jse.m06.s12;

/**
 * An abstract class. Having an abstract method it can't be concrete.
 */
public abstract class AbstractClass {
    /** an abstract method */
    public abstract void anAbstractMethod();

    /** a concrete method */
    public void aConcreteMethod() {
    }
}
